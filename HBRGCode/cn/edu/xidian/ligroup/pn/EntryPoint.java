		public static void testHBRG(int[][] Pre, int[][] Post, int[] M0,
					List<String> To, List<String> Tpri) {
		
		int m = Pre.length; // place number
		int n = Pre[1].length; // transition number
		
		//Obtain Tuo
		List<String> Tuo = new ArrayList<String>();
		for(int i = 1; i <= n; i++) {
			String tran = "t" + i;
			if(!To.contains(tran)) {
				Tuo.add(tran);
			}
		}
		
		//Obtain Tsec by removing Tpri from To
		List<String> Tsec = new LinkedList<String>(To);
		for(String tran : Tpri) {
			Tsec.remove(tran);
		}
		
		PN pn1 = new PN(Pre, Post, M0, Tuo, new ArrayList<String>());
		/*BRG brg = pn1.getBRGWithVector();
		System.out.println(brg.getNodes());
		System.out.println(brg.getArcs());*/
		// newUnobs = Tuo + Tsec
		List<String> newUnobs = new LinkedList<String>(Tuo);
		newUnobs.addAll(Tsec);
		
		PN pn2 = new PN(Pre, Post, M0, newUnobs, new ArrayList<String>());
		
		List<String> auxList = new LinkedList<String>();
		for(int i = 1; i <= n; i++) {
			auxList.add("t" + i);
		}
		
		// Permanently disable all transition t \in Tpri from pn1 
		//by first removing arcs connected with t and then adding a place with zero token as the only input place of t.
		int nPn3 = n - Tpri.size();
		int[][] pn3Pre = new int[m][nPn3];
		int[][] pn3Post = new int[m][nPn3];
		int col = 1;
		for(int i = 1; i <= n; i++) {
			String tran = "t" + i;
			if(Tpri.contains(tran)) {
				auxList.remove(tran);
			} else {
				for(int j = 1; j <=m ;j++) {
					pn3Pre[j-1][col-1] = Pre[j-1][i-1];
					pn3Post[j-1][col-1] = Post[j-1][i-1];
				}
				col++;
			}
		}
		
		//Rename unobservable transitions in pn3 according to Tuo
		List<String> pn3Tuo = new ArrayList<String>();
		for(String tran : Tuo) {
			int index = auxList.indexOf(tran);
			pn3Tuo.add("t" + (index+1));
		}
		
		PN pn3 = new PN(pn3Pre, pn3Post, M0, pn3Tuo, new ArrayList<String>());
		
		// Test HBRG		
		long startTime0 = 0;
		double estimatedTime0 = 0;
		System.out.println("===========BRG==========");
		startTime0 = System.nanoTime();
		BRG brg1 = pn1.getBRG();
		estimatedTime0 = (System.nanoTime() - startTime0)/1000000000.0;
		System.out.println("BRG size: " + brg1.getNodeNum() + " (time: " + estimatedTime0 + " s)");
		
		
		System.out.println("===========HBRG===========");
		int HBRGSize = 0;
		startTime0 = System.nanoTime();
		BRG brg2 = pn2.getBRG();
		HBRG hbrg = new HBRG(brg2);
		List<BRGNode> primaryNodes = brg2.getNodes();
		
		for(BRGNode treeNode : primaryNodes) {
			pn3.setInitialMarking(treeNode.getMarking());
			BRG brg3 = pn3.getBRGWithVector();
			hbrg.addSecondLayer(treeNode, brg3);
			HBRGSize = HBRGSize + brg3.getNodeNum();
		}
		estimatedTime0 = (System.nanoTime() - startTime0)/1000000000.0;
		
		System.out.println("HBRG size: " + HBRGSize + " (time: " + estimatedTime0 + " s)");
	}
